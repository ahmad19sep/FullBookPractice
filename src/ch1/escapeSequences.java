package ch1;

public class escapeSequences {
    public static void main(String[] args) {
        //There are 8 common escape sequences in java
        System.out.println("Hello this escape sequence used \n for next line ");
        //2nd
        System.out.println("Hello this escape sequence used  \" for quotation marks\"  ");
        //3rd
        System.out.println("Hello this escape sequence used \\ for slash \\");
        //4th
        System.out.println("Insert tab like \tthis ");
        //5th
        System.out.println("this is for back space \b like this ");
        //6
        System.out.println("Carriage return  \rlike this ");
        //7
        System.out.println("Single quotation \'like\' this ");

        //8
        System.out.println("Insert form feed like this\f i really dont know what is this lets check");
    }
}
